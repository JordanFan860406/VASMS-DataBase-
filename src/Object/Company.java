package Object;

public class Company {
	String name;
	String adress;
	
	public Company(){
		
	}
	public Company(String name,String adress){
		this.name=name;
		this.adress=adress;
	}
	public void setName(String name){
		this.name=name;
	}
	public void setAdress(String adress){
		this.adress=adress;
	}
}
