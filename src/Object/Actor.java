package Object;

public class Actor {
	String name;
	time birthday;
	String sex;
	
	public Actor(){
		
	}
	
	
	public void setName(String name){
		this.name=name;
	}
	public void setBirthday(time birthday){
		this.birthday=birthday;
	}
	public void setSex(String sex){
		this.sex=sex;
	}
	
	
	public String getName(){
		return name;
	}
}
